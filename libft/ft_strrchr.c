#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	int i;
	int l;

	i = 0;
	l = 0;
	while (s[i] != 0)
	{
		i++;
	}
	while (i >= 0)
	{
		if (s[i] == c)
		{
			return ((char *)&s[i]);
		}
		i--;
	}
	return (NULL);
}
